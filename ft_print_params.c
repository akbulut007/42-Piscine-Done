#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	while (x < argc)
	{
		while (argv[x][i] != '\0')
		{
			write(1, &argv[x][i], 1);
			i++;
		}
		i = 0;
		x++;
		write(1, "\n", 1);
	}
	return (0);
}
